public class LecturerDataModel {
	private String idSinta;
	private String namaDosen;
	private String nidnDosen;
	private String scopusHIndex;
	private String googleHIndex; 

	public String getIdSinta() {
		return idSinta;
	}
	public void setIdSinta(String idSinta) {
		this.idSinta = idSinta;
	}
	public String getNamaDosen() {
		return namaDosen;
	}
	public void setNamaDosen(String namaDosen) {
		this.namaDosen = namaDosen;
	}
	public String getNidnDosen() {
		return nidnDosen;
	}
	public void setNidnDosen(String nidnDosen) {
		this.nidnDosen = nidnDosen;
	}
	public String getGoogleHIndex() {
		return googleHIndex;
	}
	public void setGoogleHIndex(String googleHIndex) {
		this.googleHIndex = googleHIndex;
	}
	public String getScopusHIndex() {
		return scopusHIndex;
	}
	public void setScopusHIndex(String scopusHIndex) {
		this.scopusHIndex = scopusHIndex;
	}
}